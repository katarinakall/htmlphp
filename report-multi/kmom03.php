    <p>Det känns bra att komma igång med arrayer. Det är något som jag känner till sedan
    tidigare då jag har en del erfarenhet av att koda.</p>

    <p>Jag känner mig inte helt hemma med multisidan ännu. Jag har känner att det finns
    mycket kvar att lära sig, men uppgifterna har hittills gått bra så jag känner att
    jag är på rätt spår. Något som jag känner är oklart är att vad som ska ligga i
    respektive mapp och varför. I uppgiften "Bygg en multisida och testa arrayer"
    så tillkommer mappen "multipage". Jag får inte riktigt grepp på vad som skiljer
    sig mellan det filer som ligger i mappen multipage och de som ligger i mappen view.
    Vad jag kommit fram till hittills är att det som ligger i view är saker som återkommer
    på alla sidor medan det som ligger i multipage är innehåll till en sida.</p>

    <p>Jag tyckte att det blev väldigt rörigt att kombinera ihop me-sidan med multisidan och jag
    känner mig osäker på om det jag gjort är rätt. Något som jag dock blev nöjd med
    var att jag lyckades lösa ett problem med markeringen för aktuellt val i navigeringsmenyn.
    Först lyckades jag inte behålla markeringen i navigeringsmenyn om jag
    var på multisidan och sedan valde alternativ i multisidans meny. Jag löste det
    genom att istället för att kontrollera om länken slutade på "multipage.php" så
    kontrollerar jag om slutet på länken innehåller "multipage.php". Då behålls
    markeringen även när query stringen tillkommer.</p>

    <p>Jag får väldigt mycket hjälp av guiderna. Jag tycker att de är väldigt
    pedagogiska och jag föredrar att läsa guiderna över att läsa i kurslitteraturen.</p>

    <p>Min TIL för detta kursmoment är framförallt att arbeta med miljövariabler.
    Dels att känna till dem och dels kunna nyttja dem. Sedan tyckte jag om
    att lära mig hur man kan använda arrayer för att skapa ett mer dynamisk
    innehåll på webbsidan.</p>
