    <p>Jag skulle placera mig själv i kategorin erfaren nybörjare när det kommer till
        HTML/CSS. Jag hänger med i exemplena och jag tycker det är lätt att följa
        guiderna men jag tror inte jag hade klarat uppgifterna utan den tydliga
        hjälpen som vi faktiskt får. I vissa fall har jag bara kopierat CSS-en
        från exemplena och sedan ändrat i den för att på så vis komma fram till
        det resultatet jag vill ha. Något som jag dock inte lyckades med när jag
        skulle göra sidan responsiv var att fotot jag lagt in i presentationen om
        mig behövde roteras för att det låg ner. För att göra det la jag till
        "transform: rotate(90deg);". Det gav rätt resultat när jag minskade skärmen
        i webbläsaren, men när jag öppnade sidan i min mobil så låg fotot fortfarande ner.
        Utan att veta hur jag skulle lösa det på något annats sätt så valde jag tillsut
        att inte visa fotot när skärmen är liten. Jag skulle verkligen vilja få svar på
        vad jag gjorde för fel.</p>

    <p>Jag tycker om katalogstrukturen. Då jag tidigare har erfarenhet av att koda
        Java så känns det som ett bekant sätt att strukturera upp filerna.
        Jag tycker särskilt mycket om att vi lär oss att återanvända kod.
        När jag försökt mig på frontend tidigare har det blivit mycket repeterad
        kod vilket är ett gissel när man ska börja ändra i den.</p>

    <p>Jag tycker att många programmeringsspråk har mycket gemensamt. If-satser,
        for-loopar och while-loopar fungerar likadant oavsett språk. Det som
        skiljer dem åt är i huvudsak syntaxen. Det jag inte tycker om med
        php är det ständiga användandet av symbolen $. Den känns inte naturlig
        för mig. Något som jag däremot tycker väldigt mycket om är att php har
        automatisk konvertering.</p>

    <p>Än så länge har labbarna varit enkla. Jag har fastnat på några småsaker
        några gånger men det har gått relativt snabbt att lista ut vad felet varit.
        Jag känner att jag har en stor fördel av att ha programmerat sedan tidigare.</p>

    <p>Min TIL för detta kursmoment är framförallt clear float och clearfix med
        overflow auto. Jag har tidigare stött på problemen men inte vetat
        vad de beror på eller hur jag skulle gå tillväga för att lösa det.
        Det är även kul att börja jobba på att göra hemsidan dynamisk. Jag har
        tidigare använt mig av thymeleaf och känner att mycket av syntaxen
        som jag använde där liknar den som vi använder i php. Som jag
        nämnde tidigare så känns det lätt att hänga med i exemplena och följa guiderna
        men hade jag behövt skriva koden från scratch hade det inte gått lika lätt
        och smidigt.</p>
